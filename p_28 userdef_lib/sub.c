int sub(int a, int b)

{
    int subtract = 0;
    subtract = a-b;
    return subtract;
}

int add(int a, int b)

{
    int adding = 0;
    adding = a+b;
    return adding;
}

int mul(int a, int b)

{
    int multiply = 0;
    multiply = a*b;
    return multiply;
}
